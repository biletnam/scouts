                <h2 class="gray_title">Onze historie</h2>
                <h3>HET INTERBELLUM</h3>
                <ul>
                    <li><p>
                            <b>1919: </b>Zes jaar na het ontstaan van de eerste Vlaamse scoutsgroep in België (1-25e Sint-Jacob) stichtte de heer De Coninck op 15 januari 1919
                            een jongensgroep in Borgerhout die bij het latere VVKS nummer 18 als inschrijvingsnummer zou krijgen.
                        </p></li>
                    <li><p>
                            <b>1921: </b>V. L. J. Daems, een oud-leider van den 8ste St-Stanislas uit Berchem, wordt master.
                        </p></li>
                    <li><p>
                            <b>1922: </b>De groep telt exact 18 leden en heeft een kapitaal van 125 BF. Een kamp van 10 dagen kost in totaal 35 BF. 3,5 BF per dag dus !
                        </p></li>
                    <li><p>
                            <b>1923: </b>Tussen het oorlogsgewoel door tracht den 18 er over te waken geen soldatenbeweging te worden. De opvoedkundige waarde binnen den 18 wordt een verworven recht.
                            Vele Engelse termen blijven nochtans bestaan: Cubs (welpen), Scouts (jojo's en givers), Rovers, Cub-master, Scouts-master ...
                        </p></li>
                    <li><p><b>1924: </b>Master Daems sticht op 6 maart de Welpenhorde.</p></li>
                    <li><p>
                            <b>1927: </b>Master Pittoors, een leraar van St.-Norbertus uit de Groenstraat, neemt het groepsleiderschap over van Master Daems die op zijn beurt op 1 november 1927 de 2de zeescouts opricht.
                            Op 3 september overlijdt de verkenner Corneel Mayné tijdens een kamp naar Lourdes, in de bergen van het nabijgelegen Gavarnie. Zowel de 18de eenheid als de 2de zeescouts dragen
                            van dan af zijn naam om hem voor altijd te gedenken. Het groepsblad kreeg de naam "Olmenblad" mee en de lokalen, toen gelegen in de Zegelstraat, werden "Olmenheim" gedoopt.
                        </p></li>
                    <li><p><b>1935: </b>Er worden twee tenten aangekocht, elk ter waarde van 600 BF.</p></li>
                </ul><br>
                <h3>DE OORLOGSJAREN</h3>
                <ul>
                    <li><p>
                            In tegenstelling tot de Eerste Wereldoorlog werd de scoutsbeweging niet verboden.
                            Toch mocht men geen morse-oefeningen doen en mocht men geen kampen organiseren.
                            Uiteraard probeerden de Duitsers de scouting in te palmen bij de Hitlerjugend en andere pro-Duitse organisaties.
                            Zover zou het echter nooit komen. In deze oorlogsperiode hielpen vele 18'ers met het ruimen van puin en het vervoeren van gekwetsten.
                            De door master Lamquet (stichter van de Grote Trek naar Scherpenheuvel) georganiseerde zangstondes voor de geteisterden waren dikwijls fijne momenten.
                            Een voor twee derden uit 18'ers bestaande jamboreeband fleurde de gekwetsten met zang en klank op. Het is in deze oorlogsperiode dat de groep uiterlijk verandert.
                            De gele das die we tot nog toe kenden wordt vervangen door een eigen gecreëerde groepsdas in de kleuren groen-grijs.
                            Het grijs is de moeilijke zorg van elke dag en groen de glimlach van de 18-er, die kleur aan het leven geeft. We krijgen een strijdkreet: "18 B.P. WIJKT NOOIT"
                            en het groepslied "Koppen, knuisten, sterke vuisten". Het stormlied "Verkenners van 18" en "Handhaaf en Bouw", dat ooit nog als jaarlied heeft gediend, kwam van de hand van leider R. Koninckx.
                        </p></li>
                    <li><p>
                            <b>1941: </b>Vele leiders van den 18 werden omwille van de strijd in Frankrijk opgeroepen. Hierdoor moest op 23 april de verkennertak noodgedwongen worden ontbonden.
                            Op 4 oktober werd er terug gestart met twee patrouilles: de Vossen en de Doggen. Al vlug komen hier de Herten, de Valken, de Zwaluwen en de Eekhoorns bij.
                            Op vraag van master Pittoors en aalmoezenier Stoop komt R. Koninckx als leider in de groep. Er volgt een ledenstop.
                        </p></li>
                    <li><p>
                            <b>1943: </b>Lenige Bever, componist van het groepslied "Koppen, vuisten, ." wordt gefusilleerd. Ook dit jaar wordt het dragen van een uniform officieel verboden.
                            Beloftes gebeuren op een privaat terrein. Het uniform werd dan in de rugzak meegenomen.
                        </p></li>
                    <li><p><b>1944: </b>Het groepsblad "Olmenblad" wordt dit jaar omgedoopt tot "’t Schakelt je".</p></li>
                </ul><br>
