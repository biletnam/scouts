                    <li><p><b>1993: </b>De voorbereidingen voor het komende feestjaar worden gestart. In september stopt Alain Konings als adjunct groepsleider. Els Hofkens en Johan Robyns vervangen hem.</p></li>
                    <li><p>
                            <b>1994: </b>Den 18 opent op 23 januari op succesvolle wijze z'n jubel jaar "75 JAAR DEN 18". De verjaardag wordt o.a.gevierd met een jubelbal, een musical en een groepskamp te Gavarnie.
                            In september nemen Alain Konings en Mark De Bie het groepsleidingsschap over van Luc, Els en Johan.
