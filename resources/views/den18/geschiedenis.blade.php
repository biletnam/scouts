                </p>
                <h2 class="gray_title">Onze naam</h2>
                <p>
                    De naam '18BP' vindt zijn oorsprong bij de vroegste voorloper van de organisatie Scouts&amp;Gidsen Vlaanderen, waar ons de volgnummer 18 werd toegekend.
                    BP slaat uiteraard op Baden-Powell, naar sir Robert Baden-Powell, vader van het scouting.
                </p>
                <p>
                    Het tweede deel, 'Corneel Mayné', heeft echter een tragische achtergrond. Op 3 september 1927 overlijdt de verkenner Corneel Mayné
                    tijdens een kamp naar Lourdes, in de bergen van het nabijgelegen Gavarnie. Om hem voor altijd te herdenken dragen van dan af zowel
                    den 18 en de daaruit voortgekomen 2de zeescouts zijn naam.
                </p>
                <h2 class="gray_title">Onze das</h2>
                <p>
                    Aanvankelijk droeg men een effen gele das maar deze werd tijdens de Tweede Wereldoorlog vervangen door de groen-grijze groepsdas
                    die we vandaag kennen.
                </p>
                <p>Het grijs van onze das staat voor de moeilijke zorg van elke dag en het groen voor de glimlach van de 18-er.</p>
