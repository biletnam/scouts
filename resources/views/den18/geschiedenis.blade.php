                <p>
                    "Den 18BP" nadert stilaan de respectabele leeftijd van 100 (een ivoren jublieum zoals ze dat dan blijkbaar noemen).
                    Wij zijn daarom op zoek gegaan naar hoe deze laatste net-geen-100 jaar er hebben uitgezien.
                </p>
                <p>
