                <ul>
                    <li><p>
                            <b>1949: </b>Master Koninckx neemt het groepsleiderschap over. In het zelfde jaar worden de lokalen in de Langstraat gekocht.
                            Tot zover had de groep toevlucht kunnen vinden in de achterbouw van het parochiehuis in de Zegelstraat en in de toenmalige kazerne ter hoogte van Hof ter Loo.
                        </p></li>
                    <li><p><b>1955: </b>Een groepsdas kost 40 BF. Het grijze en groene stuk stof moest zelf aan elkaar worden genaaid.</p></li>
                    <li><p><b>1958: </b>Op 30 december wordt er afscheid genomen van één van de meest imposante geestelijke begeleiders van onze groep: E.P. Mertens.</p></li>
                    <li><p><b>1960: </b>In oktober komt E. P. Jan Moriaux in de groep als groepsaalmoezenier</p></li>
                    <li><p><b>1962: </b>Op 29 april wordt met behulp van 4 leidsters de kapoenentak opgericht.</p></li>
                    <li><p>
                            <b>1963: </b>De voorzitter van de oud-scouts, François (Sus)Quaeyhaegens, wordt door de groepsaalmoezenier gevraagd het groepsleiderschap over te nemen.
                            Hoewel het er oorspronkelijk op leek dat dit om een tijdelijke overname ging, zou Sus deze functie toch 20 jaar waarnemen.
                        </p></li>
                    <li><p>
                            <b>1979: </b>"60 JAAR DEN 18" wordt gevierd met een jubelreis naar Assisi in Italië, de verste trip die de groep tot nog toe heeft ondernomen.
                            Naar aanleiding van deze reis wordt onder impuls van Mare Quireyns en Alain Konings de eerste diamontage gemaakt. Ze stichtte het productieteam "Quickon".
                        </p></li>
                    <li><p><b>1981: </b>Op 13 september wordt een nieuwe tak gedoopt: de jonggidsen. De eerste takleidster wordt Ann Quaeyhaegens.</p></li>
                    <li><p><b>1982: </b>Onder impuls van Luc Van Dijck en Luc Verlinden wordt de verkennertak gemengd, we spreken nu over givers.</p></li>
                    <li><p>
                            <b>1983: </b>Hugo Monsieur volgt master Quaeyhaegens op als groepsleider. Voor het eerst in de geschiedenis van de groep gebeurt de overname via de VVKSM-spelregels,
                            d.w.z. door verkiezingen voor een mandaat van drie jaar.
                        </p></li>
                    <li><p><b>1984: </b>Johan Struyf wordt voor één jaar als groepsleider ad-interim. Het jaar daarop wordt hij verkozen voor een mandaat van drie jaar.</p></li>
                    <li><p><b>1986: </b>Tijdens het groepskamp te Martilly wordt de eerste gemengde belofteafname van jong-gidsen en jong-verkenners gehouden.</p></li>
                    <li><p><b>1987: </b>Na meer dan 30 jaar wordt de wafelenbak afgeschaft. De verkoop van St-Valentijnshyacinten komt in de plaats.</p></li>
                    <li><p><b>1988: </b>Samen met Luc Van Dijck neemt Alain Konings het groepsleiderschap over.</p></li>
                    <li><p>
                            <b>1989: </b>Op l januari wordt den 18, door het samensmelten van de jong-gidsen en jong verkenners, één van de weinige volledig gemengde groepen.
                            Op 15 januari wordt het jubeljaar "70 JAAR DEN 18" geopend. Op 29 april wordt er in Hof ter Loo een jubelbal gehouden. Een aantal oud 18-ers houden een Retro-Sing-Song.
                        </p></li>
                </ul><br>
