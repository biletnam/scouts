                    <li><p>
                            <b>1991: </b>Alain Konings en Luc Van Dijck verwisselen van functie binnen de groepsleiding. Luc wordt groepsleider en Alain wordt adjunct. Er zijn 133 aangesloten leden.
                            Er worden drie nieuwe patrouilletenten aangekocht, elk ter waarde van 40.000 BF Bij de jojo’s gaan er twee Russische meisjes uit Tsjernobyl mee naar Chanly op kamp
                        </p></li>
