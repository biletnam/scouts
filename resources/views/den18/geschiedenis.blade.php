                            <b>1999: </b>De groep bestaat 80 jaar. Dit wordt gevierd met een barbecue op linkeroever met een groot kampvuur, een misviering, een jubelbal en een jubelfoto.
                            De district jin groep 83 St-Pricilla werd opgericht. Deze groep werd gevormd door verschillende groepen van het district om een plekje te geven voor de verschillende jins voor telkens één jaar.
                        </p></li>
