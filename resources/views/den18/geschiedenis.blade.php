                            <b>2002: </b> Er wordt gewerkt aan het tentenmagazijn en het toilettenblokje. Het tentenmagazijn wordt vergroot en de toiletten worden volledig vernieuwd. Het lokaal krijgt ook een nieuwe poort.
                            Het tenten bestand wordt aanzienlijk uitgebreid met enkele nieuwe tenten. Den 18 neemt deel aan A’penkuren een groots scoutspektakel in hartje Antwerpen.
                        </p></li>
                    <li><p>
                            <b>2004: </b>De groep bestaat 85 jaar. Dit jaar werd gestart met een overgang voor alle leden, ouders en sympathisanten op het domein de Hoge Rielen. De groepsleid(st)ers zijn dit jaar
                            Tom Robyns, Geert Baetens en Katrien Beckers. Dit jaar wordt beslist ook Langstraat 12 aan te kopen (de huidige lokalen van de leiding, kapoenen en givers).
